    function pts = intersectCircles(c1,c2)
        x1 = c1(1);
        x2 = c2(1);
        y1 = c1(2);
        y2 = c2(2);
        r1 = c1(3);
        r2 = c2(3);
        d = norm([x1;y1]-[x2;y2]);
        pts = zeros(2,2);
        pts(:,1) = [(x1+x2)/2+(x2-x1)*(r1^2-r2^2)/(2*d^2)+(y2-y1)*(sqrt(((r1+r2)^2-d^2)*(d^2-(r1-r2)^2)))/(2*d^2); (y1+y2)/2+(y2-y1)*(r1^2-r2^2)/(2*d^2)-(x2-x1)*(sqrt(((r1+r2)^2-d^2)*(d^2-(r1-r2)^2)))/(2*d^2)];
        pts(:,2) = [(x1+x2)/2+(x2-x1)*(r1^2-r2^2)/(2*d^2)-(y2-y1)*(sqrt(((r1+r2)^2-d^2)*(d^2-(r1-r2)^2)))/(2*d^2); (y1+y2)/2+(y2-y1)*(r1^2-r2^2)/(2*d^2)+(x2-x1)*(sqrt(((r1+r2)^2-d^2)*(d^2-(r1-r2)^2)))/(2*d^2)];
    end